# Creating a complex number
z1 = 3 + 4j  # 3 is the real part, 4j is the imaginary part
print(z1)  

# Accessing real and imaginary parts
real_part = z1.real  # 3.0
imaginary_part = z1.imag  # 4.0

print(real_part)  
print(imaginary_part) 

# Performing arithmetic with complex numbers
z2 = 1 + 2j
z3 = z1 + z2  
print(z3)  

z4 = z1 * z2 
print(z4)  
